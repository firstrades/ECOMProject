package com.js.xml.domain;

public class Version {

	private String serviceIdText;
	private String majorText;
	private String intermediateText;
	private String minorText;
	
	
	
	public String getServiceIdText() {
		return serviceIdText;
	}
	public void setServiceIdText(String serviceIdText) {
		this.serviceIdText = serviceIdText;
	}
	public String getMajorText() {
		return majorText;
	}
	public void setMajorText(String majorText) {
		this.majorText = majorText;
	}
	public String getIntermediateText() {
		return intermediateText;
	}
	public void setIntermediateText(String intermediateText) {
		this.intermediateText = intermediateText;
	}
	public String getMinorText() {
		return minorText;
	}
	public void setMinorText(String minorText) {
		this.minorText = minorText;
	}
	
	
}
